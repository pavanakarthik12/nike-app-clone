class SneakerItem {
  final String id;
  final String name;
  final String subtitle;
  final double price;
  final String imagePath;
  final String category;
  final int featuredScore;

  const SneakerItem({
    required this.id,
    required this.name,
    required this.subtitle,
    required this.price,
    required this.imagePath,
    required this.category,
    required this.featuredScore,
  });
}
